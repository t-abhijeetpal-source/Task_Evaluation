#!/usr/bin/env bash
# B2 route extractor — regenerates a diffable map of the *real* outbound API
# surface from source, so the API map is reproducible instead of hand-asserted.
#
# Why this exists: every Retrofit method uses a dynamic `@Url url: String`, so the
# path is NOT in the annotation. But the paths ARE in source — as `const val`
# path constants (MFCommonEndPoint.kt, EquityDefaultEndpoints.kt, GtmEndpoints.kt,
# UrlsProvider.kt, …) — and the base hosts are in the keys-provider UrlsProvider.
# Full URL = <env host from UrlsProvider> + <path constant>. This script recovers
# both and emits YAML you can commit and `git diff` on every release.
#
# Usage:
#   ./extract_routes.sh [REPO_ROOT] > B2_routes.yaml
#   diff <(./extract_routes.sh) B2_routes.yaml   # detect drift in CI
set -euo pipefail

ROOT="${1:-/Users/abhijeetpal/Desktop/workspace/android-monorepo/android-monorepo}"
cd "$ROOT"

SHA="$(git rev-parse --short HEAD 2>/dev/null || echo unknown)"
PATH_RE='const val [A-Z0-9_]+ *= *"[^"]*/[^"]*"'
HOST_RE='"https://[a-z0-9.-]*paytm(money)?\.(com|in)[^"]*"'

path_constants="$(grep -rhnE "$PATH_RE" --include='*.kt' --include='*.java' . 2>/dev/null | grep -v '/build/' || true)"
host_bases="$(grep -rhoE "$HOST_RE" --include='*.kt' --include='*.java' . 2>/dev/null | grep -v '/build/' | sort -u || true)"

n_paths="$(printf '%s\n' "$path_constants" | grep -c . || true)"
n_hosts="$(printf '%s\n' "$host_bases" | grep -c . || true)"

echo "# Auto-generated by extract_routes.sh — DO NOT hand-edit. Re-run to refresh."
echo "meta:"
echo "  repo: android-monorepo"
echo "  commit: $SHA"
echo "  recovered_path_constants: $n_paths"
echo "  recovered_host_bases: $n_hosts"
echo "  note: full URL = <env host from UrlsProvider> + <path>; @Url is dynamic"
echo "hosts:"
printf '%s\n' "$host_bases" | sed 's/^"/  - "/; s/"$/"/' | sed 's/^  - ""$//' | grep . || true
echo "paths:"
# name | path  (constant name and its literal path)
printf '%s\n' "$path_constants" \
  | sed -E 's/^.*const val ([A-Z0-9_]+) *= *"([^"]+)".*$/  - name: \1\n    path: "\2"/' \
  | head -2000
